window.addEventListener("load", function () {
  class artista {
    constructor(nombre, edad, estilo, artistas) {
      this.nombre = nombre;
      this.edad = edad;
      this.estilo = estilo;
      this.artistas = artistas;
    }
  }

  class exposicion {
    constructor(titulo, fecha, descripcion) {
      this.titulo = titulo;
      this.titulo = fecha;
      this.descripcion = descripcion;
    }
  }
});
